export const textFontRegistry = textFontFamilies.map((fontFamily) => ({
    id: fontFamily,
    ...textFontDefinitionRecord[fontFamily],
}));
export const TEXT_FONT_FAMILY_STACKS = Object.fromEntries(textFontRegistry.map((definition) => [definition.id, definition.stack]));
export const TEXT_FONT_METRIC_PROFILES = Object.fromEntries(textFontRegistry.map((definition) => [definition.id, definition.metricProfile]));
export function getTextFontDefinition(fontFamily) {
    return textFontRegistry.find((definition) => definition.id === (fontFamily ?? DEFAULT_TEXT_FONT_FAMILY));
}
export function getTextFontMetricProfile(fontFamily) {
    return TEXT_FONT_METRIC_PROFILES[fontFamily ?? DEFAULT_TEXT_FONT_FAMILY];
}
