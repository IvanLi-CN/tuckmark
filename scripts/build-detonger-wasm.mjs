import { execFileSync } from "node:child_process"
import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const scriptDir = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(scriptDir, "..")
const outDir = path.join(repoRoot, "apps/web/src/wasm/pkg")
const wasmCrateDir = path.join(repoRoot, "detonger/crates/detonger-wasm")
const detongerToolchainFile = path.join(repoRoot, "detonger/rust-toolchain.toml")
const wasmModulePath = path.join(outDir, "detonger_wasm.js")
const wasmTypesPath = path.join(outDir, "detonger_wasm.d.ts")
const wasmStatusModulePath = path.join(outDir, "detonger_wasm_status.js")
const wasmStatusTypesPath = path.join(outDir, "detonger_wasm_status.d.ts")
const wasmBinaryPath = path.join(outDir, "detonger_wasm_bg.wasm")
const wasmPackRoot = path.join(repoRoot, ".tuckmark/tools/wasm-pack")
const wasmPackBin = path.join(
  wasmPackRoot,
  "bin",
  process.platform === "win32" ? "wasm-pack.exe" : "wasm-pack"
)
const wasmBuildLockPath = path.join(repoRoot, ".tuckmark/tools/.detonger-wasm-build.lock")
const args = process.argv.slice(2)
const profileFlag = args.includes("--release") ? "--release" : "--dev"
const GENERATED_BIOME_IGNORE_HEADER = "// biome-ignore-all format lint: generated by wasm-pack"

function commandExists(command, commandArgs = ["--version"]) {
  try {
    execFileSync(command, commandArgs, {
      stdio: "ignore",
    })
    return true
  } catch {
    return false
  }
}

function writeTextFile(filePath, contents) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true })
  fs.writeFileSync(filePath, contents, "utf8")
}

function addGeneratedBiomeIgnoreHeader(filePath) {
  if (!fs.existsSync(filePath)) {
    return
  }
  const contents = fs.readFileSync(filePath, "utf8")
  if (contents.startsWith(GENERATED_BIOME_IGNORE_HEADER)) {
    return
  }
  writeTextFile(filePath, `${GENERATED_BIOME_IGNORE_HEADER}\n${contents}`)
}

function annotateGeneratedTextArtifacts() {
  addGeneratedBiomeIgnoreHeader(wasmModulePath)
  addGeneratedBiomeIgnoreHeader(wasmTypesPath)
  addGeneratedBiomeIgnoreHeader(wasmStatusModulePath)
  addGeneratedBiomeIgnoreHeader(wasmStatusTypesPath)
}

function writeDetongerStatus(status) {
  const reasonLiteral = JSON.stringify(status.reason)
  writeTextFile(
    wasmStatusModulePath,
    [
      "export const detongerWasmStatus = Object.freeze({",
      `  available: ${status.available},`,
      `  reason: ${reasonLiteral},`,
      "})",
      "",
      "export default detongerWasmStatus",
      "",
    ].join("\n")
  )
  writeTextFile(
    wasmStatusTypesPath,
    [
      'import type { DetongerWasmStatus } from "../../browser-direct-path.js"',
      "",
      "declare const detongerWasmStatus: DetongerWasmStatus",
      "",
      "export { detongerWasmStatus }",
      "export default detongerWasmStatus",
      "",
    ].join("\n")
  )
}

function writeUnavailableArtifacts(reason) {
  fs.rmSync(outDir, { recursive: true, force: true })
  fs.mkdirSync(outDir, { recursive: true })
  writeDetongerStatus({
    available: false,
    reason,
  })
  writeTextFile(
    wasmModulePath,
    [
      '/* @ts-self-types="./detonger_wasm.d.ts" */',
      "",
      `const errorMessage = ${JSON.stringify(reason)}`,
      "",
      "function createUnavailableError() {",
      "  return new Error(errorMessage)",
      "}",
      "",
      "export function encodePngJobMessages() {",
      "  throw createUnavailableError()",
      "}",
      "",
      "export function encodeWidthTestMessages() {",
      "  throw createUnavailableError()",
      "}",
      "",
      "export function protocolVersion() {",
      "  throw createUnavailableError()",
      "}",
      "",
      "export function renderWidthTestPng() {",
      "  throw createUnavailableError()",
      "}",
      "",
      "export function initSync() {",
      "  throw createUnavailableError()",
      "}",
      "",
      "export default function initDetongerWasm() {",
      "  return Promise.reject(createUnavailableError())",
      "}",
      "",
    ].join("\n")
  )
  writeTextFile(
    wasmTypesPath,
    [
      "export function encodePngJobMessages(png: Uint8Array, options: any): Array<any>",
      "export function encodeWidthTestMessages(options: any): Array<any>",
      "export function protocolVersion(): string",
      "export function renderWidthTestPng(options: any, scale: number): Uint8Array",
      "",
      "export type InitInput = RequestInfo | URL | Response | BufferSource | WebAssembly.Module",
      "",
      "export interface InitOutput {}",
      "",
      "export type SyncInitInput = BufferSource | WebAssembly.Module",
      "",
      "export function initSync(module: { module: SyncInitInput } | SyncInitInput): InitOutput",
      "export default function __wbg_init(",
      "  module_or_path?:",
      "    | { module_or_path: InitInput | Promise<InitInput> }",
      "    | InitInput",
      "    | Promise<InitInput>",
      "): Promise<InitOutput>",
      "",
    ].join("\n")
  )
  annotateGeneratedTextArtifacts()
}

function ensureWasmPack() {
  if (fs.existsSync(wasmPackBin)) {
    return wasmPackBin
  }
  if (commandExists("wasm-pack")) {
    return "wasm-pack"
  }
  if (!commandExists("cargo", ["--version"])) {
    throw new Error("wasm-pack is unavailable and cargo is not installed.")
  }

  fs.mkdirSync(wasmPackRoot, { recursive: true })
  execFileSync(
    "cargo",
    ["install", "wasm-pack", "--locked", "--version", "0.14.0", "--root", wasmPackRoot],
    {
      cwd: repoRoot,
      stdio: "inherit",
    }
  )

  if (!fs.existsSync(wasmPackBin)) {
    throw new Error(`wasm-pack bootstrap did not produce ${wasmPackBin}.`)
  }

  return wasmPackBin
}

function resolveDetongerToolchain() {
  if (!fs.existsSync(detongerToolchainFile)) {
    return null
  }
  const contents = fs.readFileSync(detongerToolchainFile, "utf8")
  const match = contents.match(/channel\s*=\s*"([^"]+)"/)
  return match?.[1] ?? "stable"
}

function ensureWasmTarget(toolchain) {
  if (!commandExists("rustup", ["--version"])) {
    throw new Error("rustup is required to provision the wasm32 target for detonger-wasm.")
  }

  const installedTargets = execFileSync(
    "rustup",
    ["target", "list", "--installed", "--toolchain", toolchain],
    {
      cwd: repoRoot,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "inherit"],
    }
  )

  if (installedTargets.split(/\r?\n/).includes("wasm32-unknown-unknown")) {
    return
  }

  execFileSync("rustup", ["target", "add", "wasm32-unknown-unknown", "--toolchain", toolchain], {
    cwd: repoRoot,
    stdio: "inherit",
  })
}

function sleep(ms) {
  Atomics.wait(new Int32Array(new SharedArrayBuffer(4)), 0, 0, ms)
}

function acquireBuildLock() {
  fs.mkdirSync(path.dirname(wasmBuildLockPath), { recursive: true })
  const startedAt = Date.now()
  const timeoutMs = 120000

  while (true) {
    try {
      const fd = fs.openSync(wasmBuildLockPath, "wx")
      fs.writeFileSync(fd, `${process.pid}\n`, "utf8")
      return fd
    } catch (error) {
      const nodeError = error
      if (
        !(
          nodeError &&
          typeof nodeError === "object" &&
          "code" in nodeError &&
          nodeError.code === "EEXIST"
        )
      ) {
        throw error
      }
      if (Date.now() - startedAt > timeoutMs) {
        throw new Error(`Timed out waiting for ${wasmBuildLockPath}.`)
      }
      sleep(250)
    }
  }
}

function releaseBuildLock(fd) {
  fs.closeSync(fd)
  fs.rmSync(wasmBuildLockPath, { force: true })
}

function ensureWasmBinary() {
  if (!fs.existsSync(wasmBinaryPath)) {
    throw new Error(`detonger-wasm build did not produce ${wasmBinaryPath}.`)
  }
}

function main() {
  let buildLockFd

  try {
    if (!fs.existsSync(wasmCrateDir)) {
      const reason =
        "浏览器直连打印依赖 detonger-wasm 未生成：当前工作区缺少 detonger 子模块。恢复子模块后重新启动 dev/build。"
      writeUnavailableArtifacts(reason)
      console.warn(reason)
      return
    }
    if (!fs.existsSync(detongerToolchainFile)) {
      const reason =
        "浏览器直连打印依赖 detonger-wasm 未生成：detonger 工具链元数据缺失。恢复 detonger 子模块后重新启动 dev/build。"
      writeUnavailableArtifacts(reason)
      console.warn(reason)
      return
    }

    buildLockFd = acquireBuildLock()
    const wasmPackCommand = ensureWasmPack()
    const toolchain = resolveDetongerToolchain()
    if (!toolchain) {
      const reason =
        "浏览器直连打印依赖 detonger-wasm 未生成：detonger 工具链通道不可解析。修复后重新启动 dev/build。"
      writeUnavailableArtifacts(reason)
      console.warn(reason)
      return
    }
    ensureWasmTarget(toolchain)
    fs.rmSync(outDir, { recursive: true, force: true })
    execFileSync(
      wasmPackCommand,
      [
        "build",
        wasmCrateDir,
        "--target",
        "web",
        "--out-dir",
        outDir,
        "--out-name",
        "detonger_wasm",
        profileFlag,
      ],
      {
        cwd: repoRoot,
        stdio: "inherit",
      }
    )
    ensureWasmBinary()
    writeDetongerStatus({
      available: true,
      reason: null,
    })
    annotateGeneratedTextArtifacts()
  } catch (error) {
    const nodeError = error
    if (
      nodeError &&
      typeof nodeError === "object" &&
      "code" in nodeError &&
      nodeError.code === "ENOENT"
    ) {
      console.error("wasm-pack is required to build apps/web/src/wasm/pkg.")
    }
    throw error
  } finally {
    if (typeof buildLockFd === "number") {
      releaseBuildLock(buildLockFd)
    }
  }
}

main()
